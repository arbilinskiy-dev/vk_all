/** Известная проблема или ограничение */
export interface KnownIssue {
    /** Описание проблемы */
    description: string;
    /** Почему может возникнуть */
    reason: string;
    /** Что делать, если столкнулись */
    workaround?: string;
}

/** Тип влияния изменения на систему */
export type ImpactType = 'visual' | 'logic' | 'both';

export interface UpdateEntry {
    /** Уникальный идентификатор записи */
    id: string;
    /** Тип записи */
    type: 'bugfix' | 'feature' | 'improvement';
    /** Краткий заголовок */
    title: string;
    /** Раздел приложения, который затрагивает изменение (например, "Контент — Товары", "Автоматизации — Конкурс отзывов") */
    section?: string;
    /** Тип влияния: visual — чисто визуальное, logic — серверная/логическая часть, both — и то, и другое */
    impactType?: ImpactType;
    /** Пользовательское описание проблемы или новой возможности */
    userDescription: string;
    /** Пользовательское описание решения */
    userSolution: string;
    /** Инструкция для пользователя (для фич) */
    userInstructions?: string[];
    /** Что теперь можно сделать (для фич) */
    userBenefits?: string;
    /** Известные ограничения и возможные проблемы */
    knownIssues?: KnownIssue[];
    /** Техническое описание (сворачивается) */
    technicalDetails?: string;
}

export interface ReleaseUpdate {
    /** Дата релиза в формате ДД.ММ.ГГГГ или 'хх.хх.хххх' для незавершённых релизов */
    date: string;
    /** Краткое описание релиза */
    summary: string;
    /** Статус: опубликован или в работе */
    status: 'published' | 'in-progress';
    /** Список изменений */
    entries: UpdateEntry[];
}

