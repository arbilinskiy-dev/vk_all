/**
 * Утилита для обработки VK CDN URL фотографий товаров.
 * 
 * VK CDN URL по пути /impg/ являются ПОДПИСАННЫМИ — параметр `sign` 
 * рассчитывается на основе ВСЕХ остальных параметров (size, crop, type и т.д.).
 * Если убрать или изменить любой параметр — подпись станет невалидной и VK вернёт 403.
 * 
 * Для получения оригинального (полноразмерного) URL нужен ДРУГОЙ подписанный URL,
 * который приходит из photos[].sizes[] при синхронизации market.get(extended=1).
 * 
 * Бэкенд (format_market_item) уже извлекает лучший URL из photos[].sizes[].
 * После пересинхронизации товаров в БД будут оригинальные URL.
 */

/**
 * Проверяет, является ли VK CDN URL маленьким превью (market_thumb с crop).
 * Если да — возвращает пустую строку (товар создастся с дефолтным фото).
 * Если нет — возвращает URL без изменений.
 * 
 * Для получения оригинальных URL необходимо пересинхронизировать товары.
 */
export const getOriginalPhotoUrl = (url: string): string => {
    if (!url) return url;

    try {
        // VK CDN /impg/ URL с подписью — НЕ трогаем, возвращаем как есть
        // Подпись (sign) привязана ко ВСЕМ параметрам, их нельзя менять
        return url;
    } catch {
        return url;
    }
};
