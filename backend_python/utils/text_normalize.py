# Утилита нормализации текста для сравнения постов
# Используется в массовом редактировании для поиска совпадающих постов

import re
from typing import Optional


def normalize_text_for_comparison(text: Optional[str]) -> str:
    """
    Нормализует текст поста для корректного сравнения.
    
    Убирает VK wiki-разметку, нормализует пробелы и переносы строк,
    чтобы посты с одинаковым визуальным содержимым, но разным
    форматированием, были найдены при поиске.
    
    Обрабатывает:
    - VK-ссылки: [https://url|текст] → текст
    - VK-упоминания: [club123|текст] → текст, [id123|текст] → текст
    - VK-упоминания альт. формат: @club123 (текст) → текст
    - Глобальные переменные: {global_phone} → (удаляются)
    - Переносы строк: \r\n → \n
    - Множественные пробелы → один пробел
    - Trim пробелов в начале/конце
    """
    if not text:
        return ''
    
    result = text
    
    # 1. Нормализуем переносы строк: \r\n → \n
    result = result.replace('\r\n', '\n')
    
    # 2. Убираем VK-ссылки: [https://...|текст] → текст
    # Также [http://...|текст] → текст
    result = re.sub(r'\[https?://[^\]|]+\|([^\]]+)\]', r'\1', result)
    
    # 3. Убираем VK-упоминания в формате [type+id|текст]:
    # [club123|Название], [id456|Имя], [public789|Паблик]
    result = re.sub(r'\[(club|public|id|event)\d+\|([^\]]+)\]', r'\2', result)
    
    # 4. Убираем VK-упоминания в альтернативном формате:
    # @club123 (текст), @id456 (текст), @public789 (текст)
    result = re.sub(r'@(club|public|id|event)\d+\s*\(([^)]+)\)', r'\2', result)
    
    # 5. Убираем глобальные переменные: {global_phone}, {global_address} и т.д.
    result = re.sub(r'\{global_\w+\}', '', result)
    
    # 6. Нормализуем множественные пробелы → один (но сохраняем переносы строк)
    result = re.sub(r'[^\S\n]+', ' ', result)
    
    # 7. Убираем пустые строки (более 2 подряд переносов → 2)
    result = re.sub(r'\n{3,}', '\n\n', result)
    
    # 8. Trim пробелов в начале/конце каждой строки
    lines = result.split('\n')
    lines = [line.strip() for line in lines]
    result = '\n'.join(lines)
    
    # 9. Общий trim
    result = result.strip()
    
    return result
